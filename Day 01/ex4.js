let x = 5;
let y = "5";

console.log(x > y); // false
console.log(x < y); // false
console.log(x == y); // true (chỉ so sánh giá trị)
console.log(x === y); // false (so sánh cả kiểu dữ liệu)
console.log(x !== y); // true
